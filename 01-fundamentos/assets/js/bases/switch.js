
const dia = 2;

switch (dia) {
    case 0: 
        console.log('Domingo');
        break;
    case 1: 
        console.log('Lunes');
        break;
    case 2: 
        console.log('Martes');
        break;
    case 3: 
        console.log('Miercoles');
        break;
    default: 
        console.log('no es ningún día');
};